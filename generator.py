import numpy as np


def generate(filename, n):
    """generate n entries in phonebook file"""
    with open(filename, "w", encoding="utf") as file:
        for i in range(n):
            male = np.random.rand()

            # if male, don't change last name and get male name
            # if not, make it female name
            if male > 0.5:
                last_name = np.random.choice(last_names)
                first_name = np.random.choice(male_names)
                middle_name = np.random.choice(male_names) + "ович"
            else:
                last_name = np.random.choice(last_names) + "a"
                first_name = np.random.choice(female_names)
                middle_name = np.random.choice(male_names) + "овна"
            company_name =np.random.choice(company_names)
            work_phone = "8999"+"{:07d}".format(np.random.randint(0, 10_000_000))
            personal_phone = "8999"+"{:07d}".format(np.random.randint(0, 10_000_000))
            line = ";".join(
                [
                    last_name,
                    first_name,
                    middle_name,
                    company_name,
                    work_phone,
                    personal_phone,
                ]
            )
            file.write(line + "\n")

